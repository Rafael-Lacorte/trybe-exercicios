let numbers = [5, 9, 3, 19, 70, 8, 100, 2, 35, 27];
let sum = 0
let numberOfElements = 0
for (number of numbers){
    sum += number 
    numberOfElements += 1
}
let mean = sum/numberOfElements

if (mean > 20) {
    console.log("Value greater than 20")
    
} else {
    console.log("Value greater than 20")
}

